public class MenuProduct {
    private String productName, productMadeOF, productCategory, productType, productUM, productCurrency;
    private Double productWeight, productPrice;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductMadeOF() {
        return productMadeOF;
    }

    public void setProductMadeOF(String productMadeOF) {
        this.productMadeOF = productMadeOF;
    }

    public String getProductCategory() {
        return productCategory;
    }

    public void setProductCategory(String productCategory) {
        this.productCategory = productCategory;
    }

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public Double getProductWeight() {
        return productWeight;
    }

    public void setProductWeight(Double productWeight) {
        this.productWeight = productWeight;
    }

    public Double getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(Double productPrice) {
        this.productPrice = productPrice;
    }

    public String getProductCurrency() {
        return productCurrency;
    }

    public void setProductCurrency(String productCurrency) {
        this.productCurrency = productCurrency;
    }

    public String getProductUM() {
        return productUM;
    }

    public void setProductUM(String productUM) {
        this.productUM = productUM;
    }
}
